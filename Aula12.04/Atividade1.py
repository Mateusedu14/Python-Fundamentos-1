#AT1 - Crie um programa que solicite ao usuário a largura e altura de um retângulo (em metros), calcule a área e exiba o resultado na tela. 
#Formula: largura * altura

print("Calcule a Largura e Altura de um Retângulo")

num1 = float(input("Digite a altura do retângulo: "))
num2 = float(input("Digite a largura do retângulo: "))

resultado = num1 * num2

print(f'O resultado em metros é {resultado}m')